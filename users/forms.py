from django import forms
from django.contrib.auth.forms import ReadOnlyPasswordHashField
from .models import User

class UserCreationForm(forms.ModelForm):
    password1 = forms.CharField(label="Mot de passe", widget=forms.PasswordInput)
    password2 = forms.CharField(label="Confirmer mot de passe", widget=forms.PasswordInput)

    class Meta:
        model = User
        fields = (
            'email', 'username', 'first_name', 'last_name', 'phone', 'phone_full',
            'is_staff', 'is_superuser', 'is_active'
        )

    def clean_password2(self):
        password1 = self.cleaned_data.get("password1")
        password2 = self.cleaned_data.get("password2")
        if password1 and password2 and password1 != password2:
            raise forms.ValidationError("Les mots de passe ne correspondent pas.")
        return password2

    def save(self, commit=True):
        user = super().save(commit=False)
        user.set_password(self.cleaned_data["password1"])
        if commit:
            user.save()
        return user

class UserChangeForm(forms.ModelForm):
    password = ReadOnlyPasswordHashField(label="Mot de passe",
        help_text="Vous pouvez changer le mot de passe en utilisant <a href=\"../password/\">ce formulaire</a>.")

    class Meta:
        model = User
        fields = (
            'email', 'username', 'first_name', 'last_name', 'phone', 'phone_full',
            'password', 'is_active', 'is_staff', 'is_superuser', 'groups', 'user_permissions'
        )
